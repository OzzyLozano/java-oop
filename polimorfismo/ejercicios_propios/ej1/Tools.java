package ejercicios_propios.ej1;

public class Tools {
  public double damage;
  public int durability;
  public String tool;
  public int mine;

  public void setDamage(double d) {
    damage = d;
  }

  public void setTool(String t) {
    tool = t;
  }

  public void setDurability(int d) {
    durability = d;
  }

  public void setMineSpeed(int ms) {
    mine = ms;
  }
}
